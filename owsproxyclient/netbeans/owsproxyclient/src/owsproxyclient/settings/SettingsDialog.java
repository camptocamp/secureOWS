    public List<Object> getState() {
        ArrayList<Object> state = new ArrayList<Object>();

        for (int i = 0; i < jTabbedPane1.getTabCount(); i++) {
            SettingsPanel panel = (SettingsPanel) jTabbedPane1.getComponentAt(i);
            state.add(panel.getState());
        }

        return state;
    }

